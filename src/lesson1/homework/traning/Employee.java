package lesson1.homework.traning;

public class Employee {
    private String name;

    public Employee(String name) {
        this.name = name;
    }

    // Getter：提供 name 屬性的讀取方式
    public String getName() {
        return name;
    }

    // Setter：提供 name 屬性的設定方式
    public void setName(String name) {
        this.name = name;
    }

}
